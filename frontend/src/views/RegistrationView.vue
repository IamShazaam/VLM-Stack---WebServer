<script>
import axios from 'axios';

export default {
  data() {
    return {
      firstName: '',
      lastName: '',
      username: '',
      usernameStatus: '',
      email: '',
      emailStatus: '',
      password: '',
      genre: null,
    };
  },
  computed: {
    passwordLengthError() {
      return this.password.length >= 10;
    },
  },
  methods: {
    checkUsername() {
      if (this.username.length < 3) {
        this.usernameStatus = '';
        return;
      }

      this.usernameStatus = ' username';
      console.log('we are checking this' + this.usernameStatus);

      axios
        .get(`http://localhost:8000/api/check-username/${this.username}`)
        .then((response) => {
          if (response.data.boolean === 0) {
            this.usernameStatus = 'exists';
            console.log(
              "This username '" + this.username + "'" + this.usernameStatus
            );
          } else {
            this.usernameStatus = 'available';
            console.log(
              "This username '" + this.username + "' is " + this.usernameStatus
            );
          }
        })
        .catch((error) => {
          console.log(error);
        });
    },

    checkEmail() {
      if (this.email.length < 3) {
        this.emailStatus = '';
        return;
      }

      this.emailStatus = 'email';
      console.log('we are checking this ' + this.emailStatus);

      axios
        .get(`http://localhost:8000/api/check-email/${this.email}`)
        .then((response) => {
          if (response.data.boolean === 0) {
            this.emailStatus = 'exists';
            console.log("This email '" + this.email + "' " + this.emailStatus);
          } else {
            this.emailStatus = 'available';
            console.log(
              "This email '" + this.email + "' is " + this.emailStatus
            );
          }
        })
        .catch((error) => {
          console.log(error);
        });
    },

    register() {
      this.submitted = true; // Set the submitted flag to true
      if (
        !this.firstName ||
        !this.lastName ||
        !this.username ||
        !this.email ||
        !this.password ||
        !this.genre
      ) {
        return;
      }
      const genreBoolean = this.genre === '1';
      axios
        .post('http://localhost:8000/api/create-account', {
          firstName: this.firstName,
          lastName: this.lastName,
          username: this.username,
          email: this.email,
          password: this.password,
          genre: genreBoolean,
        })
        .then((response) => {
          // Handle successful registration
          console.log(response.data);
          this.errorMessage = '';
          this.successMessage = 'Account created successfully';
        })
        .catch((error) => {
          // Handle registration error
          console.log(error.response.data);
        });
    },
  },
};
</script>
